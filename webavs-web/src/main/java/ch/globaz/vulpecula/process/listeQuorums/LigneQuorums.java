package ch.globaz.vulpecula.process.listeQuorums;

public class LigneQuorums {

    private String noAffilie;
    private String NSS;
    private String codeConvention;
    private String qualification;
    private String langue;
    private String syndicat;

    public String getNoAffilie() {
        return noAffilie;
    }

    public void setNoAffilie(String noAffilie) {
        this.noAffilie = noAffilie;
    }

    public String getNSS() {
        return NSS;
    }

    public void setNSS(String nSS) {
        NSS = nSS;
    }

    public String getCodeConvention() {
        return codeConvention;
    }

    public void setCodeConvention(String codeConvention) {
        this.codeConvention = codeConvention;
    }

    public String getQualification() {
        return qualification;
    }

    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    public String getLangue() {
        return langue;
    }

    public void setLangue(String langue) {
        this.langue = langue;
    }

    public String getSyndicat() {
        return syndicat;
    }

    public void setSyndicat(String syndicat) {
        this.syndicat = syndicat;
    }

}
