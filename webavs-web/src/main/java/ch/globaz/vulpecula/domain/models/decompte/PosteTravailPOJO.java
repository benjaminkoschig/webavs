package ch.globaz.vulpecula.domain.models.decompte;

public class PosteTravailPOJO {

    private String idTravailleur;
    private String correTra;
    private String correPost;
    private String idPost;

    public String getIdTravailleur() {
        return idTravailleur;
    }

    public void setIdTravailleur(String idTravailleur) {
        this.idTravailleur = idTravailleur;
    }

    public String getCorreTra() {
        return correTra;
    }

    public void setCorreTra(String correTra) {
        this.correTra = correTra;
    }

    public String getCorrePost() {
        return correPost;
    }

    public void setCorrePost(String correPost) {
        this.correPost = correPost;
    }

    public String getIdPost() {
        return idPost;
    }

    public void setIdPost(String idPost) {
        this.idPost = idPost;
    }

}
