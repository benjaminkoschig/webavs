package globaz.aquila.pojo;

public class AdresseJournalContentieuxExcelmlPojo {

    private String adresseContentieux;
    private String adresseDomicile;
    private String adresseCourrier;
    private String cantonAdresseContentieux;
    private String cantonAdresseDomicile;
    private String cantonAdresseCourrier;

    public AdresseJournalContentieuxExcelmlPojo() {
        super();
        adresseContentieux = "";
        adresseDomicile = "";
        adresseCourrier = "";
        cantonAdresseContentieux = "";
        cantonAdresseDomicile = "";
        cantonAdresseCourrier = "";
    }

    public String getAdresseContentieux() {
        return adresseContentieux;
    }

    public void setAdresseContentieux(String adresseContentieux) {
        this.adresseContentieux = adresseContentieux;
    }

    public String getAdresseDomicile() {
        return adresseDomicile;
    }

    public void setAdresseDomicile(String adresseDomicile) {
        this.adresseDomicile = adresseDomicile;
    }

    public String getAdresseCourrier() {
        return adresseCourrier;
    }

    public void setAdresseCourrier(String adresseCourrier) {
        this.adresseCourrier = adresseCourrier;
    }

    public String getCantonAdresseContentieux() {
        return cantonAdresseContentieux;
    }

    public void setCantonAdresseContentieux(String cantonAdresseContentieux) {
        this.cantonAdresseContentieux = cantonAdresseContentieux;
    }

    public String getCantonAdresseDomicile() {
        return cantonAdresseDomicile;
    }

    public void setCantonAdresseDomicile(String cantonAdresseDomicile) {
        this.cantonAdresseDomicile = cantonAdresseDomicile;
    }

    public String getCantonAdresseCourrier() {
        return cantonAdresseCourrier;
    }

    public void setCantonAdresseCourrier(String cantonAdresseCourrier) {
        this.cantonAdresseCourrier = cantonAdresseCourrier;
    }

}
